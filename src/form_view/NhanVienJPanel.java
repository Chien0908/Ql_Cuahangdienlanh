    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if(KiemTraTXT()  ){
            if(JOptionPane.showConfirmDialog(this, "Bạn có chắc muốn thêm không?", "Thông báo", JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION){
                String sql = "insert into nhanvien( hoten, sdt,taikhoan,matkhau,chucvu) values ( N'"+jTextField2.getText().trim()+"', N'"+jTextField3.getText().trim()+"', N'"+jTextField4.getText().trim()+"', N'"+jTextField5.getText().trim()+"', N'"+jTextField6.getText().trim()+"')";
                int kq = NhanVienDAO.ThemXoaSuaNV(sql);
                if(kq!=-1)
                {
                    dsnv = NhanVienDAO.getDSNV();
                    laydulieuban(dsnv);
                }
                else
                JOptionPane.showMessageDialog(this, "Mã nhân viên này đã tồn tại, không thể thêm", "Thông báo", 2);

            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        int i = jTable1.getSelectedRow();

        if(JOptionPane.showConfirmDialog(this, "Bạn có chắc muốn xóa không?", "Thông báo", JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION){
            String sql = "delete from nhanvien where manv = '"+jTextField1.getText().trim()+"'";
            if(NhanVienDAO.ThemXoaSuaNV(sql)!=-1)
            {
                dsnv = NhanVienDAO.getDSNV();
                laydulieuban(dsnv);
            }
            else
            JOptionPane.showMessageDialog(this, "Dữ liệu này đang được sử dụng, không thể xóa", "Thông báo", 2);

        }
    }//GEN-LAST:event_jButton2ActionPerformed

