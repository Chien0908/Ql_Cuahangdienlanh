    public void laydulieuban(ArrayList<NhanVien> dsnv){
        tblData.removeAllElements();
        for(NhanVien b : dsnv){
            Vector v = new Vector();
            v.add(b.getManv()); v.add(b.getHoten()); v.add(b.getSdt());v.add(b.getTaikhoan()); v.add(b.getMatkhau());   v.add(b.getChucvu()); 
            tblData.add(v);
        }
        jTable1.setModel(new DefaultTableModel(tblData, tblTitle));
    }
